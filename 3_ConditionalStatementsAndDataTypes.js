let str = "Test string"
let bool = true
let num = 10

console.log(`String + boolean = ${str + bool}`)
console.log(`String + number = ${str + num}`)
console.log(`Number + boolean = ${num + bool}`)

console.log(`String * boolean = ${str * bool}`)
console.log(`String * number = ${str * num}`)
console.log(`Number * boolean = ${num * bool}`)

console.log(`String / boolean = ${str / bool}`)
console.log(`String / number = ${str / num}`)
console.log(`Number / boolean = ${num / bool}`)

console.log(`String(boolean) = ${String(bool)}`)
console.log(`String(number) = ${String(num)}`)
console.log(`Boolean(string) = ${Boolean(str)}`)
console.log(`Boolean(number) = ${Boolean(num)}`)
console.log(`Number(string) = ${Number(str)}`)
console.log(`Number(boolean) = ${Number(bool)}`)